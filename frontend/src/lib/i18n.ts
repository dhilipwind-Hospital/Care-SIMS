export const LANGUAGE_OPTIONS: { code: Language; label: string; nativeLabel: string }[] = [
  { code: 'en', label: 'English', nativeLabel: 'EN' },
  { code: 'hi', label: 'Hindi', nativeLabel: 'हि' },
  { code: 'ta', label: 'Tamil', nativeLabel: 'த' },
];

const STORAGE_KEY = 'hms_lang';

export function getStoredLanguage(): Language {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored === 'en' || stored === 'hi' || stored === 'ta') return stored;
  } catch (err) { console.error('Failed to read stored language:', err); }
  return 'en';
}

export function storeLanguage(lang: Language): void {
  try {
    localStorage.setItem(STORAGE_KEY, lang);
  } catch (err) { console.error('Failed to store language:', err); }
}

